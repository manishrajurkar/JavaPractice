package PracticeAssignment.Inerviews;

public class PrintNaturalNumbers {
    /*
    Print natural numbers sum
     */

    public static void main (String args[]){

        int n=4;
        int sum =0;

        for (int i =0; i<=n; i++){

            sum +=i;
        }
        System.out.println("SUM OF 4 NATURAL NUMBERS " +sum);
    }
}
